# Исполнитель Редактор получает на вход строку цифр и преобразовывает её. Редактор может выполнять две команды, в обеих командах v и w 
# обозначают цепочки символов.
# 1. заменить (v, w) 
# 2. нашлось (v)
# Первая команда заменяет в строке первое слева вхождение цепочки v на цепочку w. Если цепочки v в строке нет, эта команда не изменяет строку.
#  Вторая команда проверяет, встречается ли цепочка v в строке исполнителя Редактор.
# Дана программа для исполнителя Редактор:
# ПОКА нашлось (22222) ИЛИ нашлось (9999)
#   ЕСЛИ нашлось (22222)
#     ТО заменить (22222, 99)
#     ИНАЧЕ заменить (9999, 2)
#   КОНЕЦ ЕСЛИ
# КОНЕЦ ПОКА
# Какая строка получится в результате применения приведённой ниже программы к строке, состоящей из 136 идущих подряд цифр 9? 
# В ответе запишите полученную строку.

def editor(s):
    while "22222" in s or "9999" in s:
        if "22222" in s:
            s = s.replace("22222", "99", 1)  # Заменяем первое вхождение "22222" на "99"
        else:
            s = s.replace("9999", "2", 1)  # Заменяем первое вхождение "9999" на "2"
    return s

# Стартовая строка из 136 цифр 9
start_line = "9" * 136
result = editor(start_line)
print(result)