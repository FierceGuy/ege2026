# На вход алгоритма подаётся натуральное число N. Алгоритм строит по нему новое число R следующим образом.
# 1. Строится двоичная запись числа N.
# 2. Далее эта запись обрабатывается по следующему правилу:
#   а) если число чётное, то к двоичной записи числа слева дописывается 10;
#   б) если число нечётное, то к двоичной записи числа слева дописывается 1 и справа дописывается 01.
# Полученная таким образом запись (в ней на два или три разряда больше, чем в записи исходного числа N) является двоичной записью искомого числа R.
# Например, для исходного числа 4 = 1002 результатом является число 20 = 101002, а для исходного числа 5 = 1012 это число 53 = 1101012.
# Укажите максимальное число R, которое может быть результатом работы данного алгоритма, при условии, что N не больше 12.
# В ответе запишите это число в десятичной системе счисления.

def f(N):
    R = bin(N)[2:]
    if N%2 ==0:
        R= '10' + R
    else:
        R = '1' + R + '01'

    R=int(R, 2)
    return R

Rlist =[]
for N in range (13):
    Rlist.append(f(N))
print(max(Rlist))